package com.sigebi.clases;

public class UnionEstamentos {
		
	private String nombreestamento;
	private String coddepartamento; 
	private String codcarrera;
	private Integer femenino; 
	private Integer masculino; 
	private Integer suma;
	public String getNombreestamento() {
		return nombreestamento;
	}
	public void setNombreestamento(String nombreestamento) {
		this.nombreestamento = nombreestamento;
	}
	public String getCoddepartamento() {
		return coddepartamento;
	}
	public void setCoddepartamento(String coddepartamento) {
		this.coddepartamento = coddepartamento;
	}
	public Integer getFemenino() {
		return femenino;
	}
	public void setFemenino(Integer femenino) {
		this.femenino = femenino;
	}
	public Integer getMasculino() {
		return masculino;
	}
	public void setMasculino(Integer masculino) {
		this.masculino = masculino;
	}
	public Integer getSuma() {
		return suma;
	}
	public void setSuma(Integer suma) {
		this.suma = suma;
	}
	public String getCodcarrera() {
		return codcarrera;
	}
	public void setCodcarrera(String codcarrera) {
		this.codcarrera = codcarrera;
	} 
    
	
}